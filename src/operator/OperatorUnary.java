package operator;

public class OperatorUnary {
    public static void main(String[] args) {
        int i = 0;
        i++; //i increment = 1
        System.out.println(i);
        i++; //i increment = 2
        System.out.println(i);
        i=i+1; //i increment = 3
        System.out.println(i);
    }
}
